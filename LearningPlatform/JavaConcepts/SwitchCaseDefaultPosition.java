public class SwitchCaseDefaultPosition {
    public static void main(String[] args) {
		int k = 1;
		switch (k) {
		default:
			System.out.println("Hello");
		case 1:
			System.out.println("Welcome");
		case 2:
			System.out.println("To");
		case 3:
			System.out.println("Java");
			break;
        }
	}
}
