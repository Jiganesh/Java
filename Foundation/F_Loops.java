public class F_Loops {
    public static void main(String[] args) {
        

        // This is a while loop
        int i = 0;

        while ( i < 10){
            System.out.println(i);
            i++;
        }

        // This is a for loop
        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }
    }
}
